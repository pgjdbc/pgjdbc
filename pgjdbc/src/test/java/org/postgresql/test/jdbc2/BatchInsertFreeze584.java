  private static PreparedStatement getStatement(Connection conn, int block) throws SQLException {
    int index;
    switch (block) {
      case 1:
        index = 0;
        break;
      case 2:
        index = 1;
        break;
      case 4:
        index = 2;
        break;
      case 8:
        index = 3;
        break;
      case 16:
        index = 4;
        break;
      case 32:
        index = 5;
        break;
      case 64:
        index = 6;
        break;
      case 128:
        index = 7;
        break;
      default:
        throw new IllegalStateException();
    }
    PreparedStatement pst = blocks[index];
    if (pst == null) {
      StringBuilder sb = new StringBuilder();
      sb.append("INSERT INTO BugFreezeTable (Col1,Col2,Col3,Col4,Col5,Col6,Col7,Col8) VALUES ");
      for (int i = 0; i < block; i++) {
        if (i > 0) {
          sb.append(',');
        }
        sb.append("(?,?,?,?,?,?,?,?)");
      }
      String insert = sb.toString();
      pst = conn.prepareStatement(insert);
      blocks[index] = pst;
    }
    return pst;
  }

